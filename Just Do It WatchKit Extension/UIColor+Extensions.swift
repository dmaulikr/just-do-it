import UIKit

extension UIColor {
    class func pausedColor() -> UIColor {
        return UIColor(red: 194.0/255.0, green: 203.0/255.0, blue: 206.0/255.0, alpha: 1.0)
    }
    
    class func runningColor() -> UIColor {
        return UIColor(red: 98.0/255.0, green: 230.0/255.0, blue: 34.0/255.0, alpha: 1.0)
    }
}
